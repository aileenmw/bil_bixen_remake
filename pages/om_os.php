    <h1>Om os</h1>
    Autopunkt.dk A/S har eksisteret på det danske marked siden 2008 og har udviklet 
    sig mod en god markedsposition med kerneværdier som tillid, ærlighed, gode priser 
    og top kvalitets biler.
    Vores motto lyder “Bilen De ønsker finder vi!” Vi finder lige præcis den bil du 
    ønsker og som passer til dine behov.<br><br>
    Vi gør alt for at yde dig den bedste og mest professionelle service samt at 
    kundeplejen er i top, så du altid er 100 % i fokus. Vi har åbent 6 dage om ugen og 
    telefonen er tændt alle ugens dage 24 timer i døgnet. Vi ønsker at vores forretning 
    fremstår eksklusiv, lys og imødekommende, så du altid kan føle dig velkommen og veltilpas. 
    Det er vigtigt for os, at ikke bare bilen er god, men også at hele oplevelsen 
    omkring handlen skal være rigtig.<br><br>a
    Vi beskæftiger os hovedsageligt med køb og salg af nyere brugte biler, finansiering, 
    samt kommission. Da vi ikke er afhængige af importører, forhandlere og mærker 
    kan vi sikre dig, at du får lige præcis den bil, som passer til dig og dine behov 
    og til den helt rigtige pris.
